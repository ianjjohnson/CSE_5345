#OTP -- Used to stand for Open Telecom Platform -- now it's just a name
#A set of conventions, libraries, and tools
#Over 25 years old and approaching its 20th release

#Solves problems of telecoms software:
#scalability
#fault-tolerance -- lives depend on it
#parallelism
#zero-downtime deployment
#These problems exist for almost all server-side software

#The basic coding abstraction is the "behaviour" (British spelling)
#It's a little bit like an interface in Java or an abstract base class
